# Following the function design recipe, define a function that has one parameter, a number, and returns that number tripled.

def numberTripled(num: int) -> int :
    """
        This function takes one argument numberTripled(arg), the argument should be an integer.
        The return result of the function is the arg times 3.
        Example :
            >>> numberTripled(3)
            9
            >>> 
            Results in return value of 9
    """
    return num * 3


